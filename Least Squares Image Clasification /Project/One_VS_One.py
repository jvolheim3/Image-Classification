def one_Vs_oneTrain(Xdic):
    '''
    Function preforms the training
    :param Xdic: dictionary of images seperated by labels
    :return: Theta matrix
    '''
    # *******************************************************************************************
    # List to hold thetas
    # *******************************************************************************************
    theta = []
    # *******************************************************************************************
    # loop through 10 choose 2 combinations
    # *******************************************************************************************
    for i in range(10):
        for j in range(i + 1, 10):
            # ***********************************************************************************
            # Create a Y that has ones stacked on top and -1 elsewhere
            # ***********************************************************************************
            temp = np.append(np.ones((len(Xdic[str(i)]))), np.ones((len(Xdic[str(j)]))) * (-1))
            yt = np.reshape(temp, (-1, 1)).transpose()
            # ***********************************************************************************
            # Create X that has the corisponding positive one labeled data on top and the negative
            # 1 on bottom
            # ***********************************************************************************
            x = np.append(Xdic[str(i)], Xdic[str(j)], axis=0)
            # ***********************************************************************************
            # Preform theta = X^t * Y to calculate theta values
            # ***********************************************************************************
            t = np.matmul(np.linalg.pinv(x), yt.transpose())
            # ***********************************************************************************
            # Append trained thetas to list and train the next set if not finished
            # ***********************************************************************************
            theta.append(t)
    # *******************************************************************************************
    # reshaping matrix
    # *******************************************************************************************
    theta = np.asarray(theta)
    c = len(Xdic['0'][0])
    theta = theta.reshape(45, c)
    # *******************************************************************************************
    # Return theta matrix
    # *******************************************************************************************
    return theta


